package chapter02;

public class FloatDoubleExample {
	public static void main(String[] args) {
		float var1 = 3.14f;
		float var2 = 3.14f;
		double var3 = 3.14;
		
		// 정밀도 테스트
		float var4 = 0.123456789f;
		double var5 = 0.123456789;
		System.out.println(var4);
		System.out.println(var5);
		
		
	}
}
